<?php

declare(strict_types = 1);

namespace chabior\Lock\ValueObject;

class LockName
{
    /**
     * @var string
     */
    private $name;

    public function __construct(string $name)
    {
        $this->name = $name;
    }

    public function getName(): string
    {
        return $this->name;
    }
}
